package jfitness.person.fitness.data;

/**
 * This enum represents a person's biological sex, either male or female.
 *
 * <p>
 * An individual's sex is necessary to perform a varied amount of operations
 * within health and fitness like for example the calculation of an individual
 * basal metabolic rate, since in most of cases, it differs between women and
 * men.
 * </p>
 *
 * @author Saúl Rodríguez Naranjo
 */
public enum BiologicalSex
{
    MAN,
    WOMAN
}
